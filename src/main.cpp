void loop() {
  unsigned long currentMillis = millis();

  // Update OLED animation
  if (currentMillis - lastUpdate > 50 && frameIndex < totalFrames) {  
      lastUpdate = currentMillis;
        u8g2.clearBuffer();
        u8g2.drawXBMP(25, 15, 80, 40, epd_bitmap_allArray[frameIndex]);
        u8g2.sendBuffer();
        frameIndex++;
    }

    if (playing && noteIndex < notes * 2) {
      int divider = melody[noteIndex + 1];
      int noteDuration = (divider > 0) ? (wholenote / divider) : (wholenote / abs(divider) * 1.5);
      
      tone(BUZZER_PIN, melody[noteIndex], noteDuration * 0.9);
      delay(noteDuration);
      noTone(BUZZER_PIN);

      noteIndex += 2;
  }
  if (frameIndex >= totalFrames && noteIndex >= notes * 2) {
    playing = false;
  }
}
